	/**
	 * Set of settings for icon param
	 *
	 * @param $id
	 * @param $selector
	 * @param $dynamic
	 * @param bool $style
	 *
	 * @return array
	 */
	public static function get_icon_control( $id, $selector, $dynamic, $style = true ) {
		$svg_param = array(
			'id'          => $id . '_svg',
			'title'       => __( 'Icon HTML Code', 'pagex' ),
			'description' => __( 'Insert inline SVG code or inline HTML with encoded image. If you wish SVG inherits color from element settings add attribute fill="currentColor" to each SVG path.', 'pagex' ),
			'type'        => 'textarea',
			'condition'   => array(
				$id => array( 'svg' )
			),
		);

		if ( ! $dynamic ) {
			$svg_param['action']   = 'content';
			$svg_param['selector'] = $selector;
		}

		if ( $style ) {
			$icon_style = array(
				array(
					'id'         => $id . '_size',
					'title'      => __( 'Icon Size', 'pagex' ),
					'type'       => 'text',
					'responsive' => true,
					'action'     => 'css',
					'class'      => 'col-4',
					'selector'   => '[el] ' . $selector . ' .pagex-icon {width: [val]; height: [val]; font-size: [val]}',
					'condition'  => array(
						$id => array( 'font-awesome', 'svg', 'image' )
					)
				),
				array(
					'id'         => $id . '_padding',
					'title'      => __( 'Icon Padding', 'pagex' ),
					'type'       => 'text',
					'responsive' => true,
					'action'     => 'css',
					'class'      => 'col-4',
					'selector'   => '[el] ' . $selector . ' {padding: [val]}',
					'condition'  => array(
						$id => array( 'font-awesome', 'svg', 'image' )
					)
				),
				array(
					'id'         => $id . '_margin',
					'title'      => __( 'Icon Margin', 'pagex' ),
					'type'       => 'text',
					'responsive' => true,
					'action'     => 'css',
					'class'      => 'col-4',
					'selector'   => '[el] ' . $selector . ' {margin: [val]}',
					'condition'  => array(
						$id => array( 'font-awesome', 'svg', 'image' )
					)
				),
			);
		} else {
			$icon_style = array();
		}

		$basic_icon = array(
			array(
				'id'      => $id,
				'title'   => __( 'Icon Type', 'pagex' ),
				'type'    => 'select',
				'options' => array(
					''             => __( 'None', 'pagex' ),
					'font-awesome' => 'Font Awesome',
					'svg'          => __( 'SVG or Base 64', 'pagex' ),
					'image'        => __( 'Image', 'pagex' ),
				),
			),
			array(
				'id'        => $id . '_fa',
				'title'     => __( 'Font Awesome Icon', 'pagex' ),
				'type'      => 'iconpicker',
				'condition' => array(
					$id => array( 'font-awesome' )
				),
				'options'   => Pagex_Editor_Control_Attributes::get_font_awesome(),
			),

			$svg_param,

			array(
				'id'        => $id . '_image',
				'title'     => __( 'Icon Image', 'pagex' ),
				'type'      => 'image',
				'sizes'     => true,
				'condition' => array(
					$id => array( 'image' )
				),
			),
		);

		$basic_icon = array_merge( $basic_icon, $icon_style );

		return $basic_icon;
	}

